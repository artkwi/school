 NAUCZYCIEL VARCHAR(11) FOREIGN KEY REFERENCES NAUCZYCIELE(PESEL) CHECK (NAUCZYCIEL LIKE '[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]') NOT NULL,
 KLASA VARCHAR(10) FOREIGN KEY REFERENCES KLASY(KLASA) NOT NULL,
 GODZINA TIME(7) NOT NULL,
 DZIEN VARCHAR(45) NOT NULL
 )


 
IF OBJECT_ID('UCZNIOWIE','U') IS NULL 
CREATE TABLE UCZNIOWIE (				
 PESEL VARCHAR(11) PRIMARY KEY CHECK (PESEL LIKE '[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]'),
 IMIE VARCHAR(45) NOT NULL,
 NAZWISKO VARCHAR(45) NOT NULL,
 KLASA VARCHAR(10) FOREIGN KEY REFERENCES KLASY(KLASA) NOT NULL,
 DATA_URODZENIA DATE,
 MIEJSCE_URODZENIA VARCHAR(45),
 ZAMESZKANIE VARCHAR(45)
 )

IF OBJECT_ID('OCENY','U') IS NULL 
CREATE TABLE OCENY (				
 ID INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
 ZAJECIA INT FOREIGN KEY REFERENCES ZAJECIA(ID) NOT NULL,
 UCZEN VARCHAR(11) FOREIGN KEY REFERENCES UCZNIOWIE(PESEL) CHECK (UCZEN LIKE '[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]') NOT NULL,
 OCENA FLOAT,
 DATA_W DATE NOT NULL,
 OPIS VARCHAR(250) NOT NULL
 )


